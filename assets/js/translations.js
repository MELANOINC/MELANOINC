// Translation utility
window.MELANO_I18N = {
    currentLang: 'es',
    
    // Get translation
    t(key, lang = null) {
        const targetLang = lang || this.currentLang;
        const keys = key.split('.');
        let value = window.MELANO_TRANSLATIONS[targetLang];
        
        for (const k of keys) {
            if (value && typeof value === 'object' && k in value) {
                value = value[k];
            } else {
                // Fallback to Spanish if key not found
                value = window.MELANO_TRANSLATIONS.es;
                for (const fallbackKey of keys) {
                    if (value && typeof value === 'object' && fallbackKey in value) {
                        value = value[fallbackKey];
                    } else {
                        console.warn(`Translation key not found: ${key}`);
                        return key;
                    }
                }
                break;
            }
        }
        
        return typeof value === 'string' ? value : key;
    },
    
    // Apply translations to DOM
    applyTranslations(lang = null) {
        const targetLang = lang || this.currentLang;
        
        // Update data-i18n elements
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key, targetLang);
            element.innerHTML = translation;
        });
        
        // Update placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.t(key, targetLang);
            element.placeholder = translation;
        });
        
        // Update lang-specific elements
        document.querySelectorAll('[data-lang]').forEach(element => {
            const elementLang = element.getAttribute('data-lang');
            if (elementLang === targetLang) {
                element.style.display = 'inline-flex';
            } else {
                element.style.display = 'none';
            }
        });
    },
    
    // Switch language
    switchLanguage(lang) {
        if (window.MELANO_TRANSLATIONS[lang]) {
            this.currentLang = lang;
            document.documentElement.setAttribute('data-theme', lang);
            this.applyTranslations(lang);
            
            // Update language buttons
            document.querySelectorAll('.lang-btn').forEach(btn => {
                btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
            });
            
            // Save preference
            localStorage.setItem('melano_lang', lang);
            
            // Track language change
            if (window.MELANO_UTILS) {
                window.MELANO_UTILS.trackEvent('language_change', { language: lang });
            }
        }
    },
    
    // Initialize
    init() {
        // Load saved language preference
        const savedLang = localStorage.getItem('melano_lang') || 
                         navigator.language.startsWith('en') ? 'en' : 'es';
        
        this.switchLanguage(savedLang);
    }
};